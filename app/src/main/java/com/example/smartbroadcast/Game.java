package com.example.smartbroadcast;

public class Game {
    private String game11;
    private String game12;
    private String game22;
    private String game21;

    public Game() {
    }

    public Game(String game11, String game12, String game22, String game21) {
        this.game11 = game11;
        this.game12 = game12;
        this.game22 = game22;
        this.game21 = game21;
    }

    public String getGame11() {
        return game11;
    }

    public void setGame11(String game11) {
        this.game11 = game11;
    }

    public String getGame12() {
        return game12;
    }

    public void setGame12(String game12) {
        this.game12 = game12;
    }

    public String getGame22() {
        return game22;
    }

    public void setGame22(String game22) {
        this.game22 = game22;
    }

    public String getGame21() {
        return game21;
    }

    public void setGame21(String game21) {
        this.game21 = game21;
    }
}
